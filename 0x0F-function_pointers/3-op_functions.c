#include "3-calc.h"
/**
 * op_add - function to add
 * @a: num1
 * @b: num2
 * Return: returns the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function to subtract
 * @a: num1
 * @b: num2
 * Return: returns the difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function to multiply
 * @a: num1
 * @b: num2
 * Return: returns the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function to divide
 * @a: num1
 * @b: num2
 * Return: returns the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function to get modulo
 * @a: num1
 * @b: num2
 * Return: returns the modulo of a from b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
