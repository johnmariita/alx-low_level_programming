#include <stdlib.h>
#include "calc.h"

/**
 * op_add - function that adds two nums
 * @a: the first number
 * @b: the second number
 * Return: returns the sum of the digits
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that subtracts two nums
 * @a: the first number
 * @b: the second number
 * Return: returns the difference of the digits
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiplies two nums
 * @a: the first number
 * @b: the second number
 * Return: returns the product of the digits
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides two nums
 * @a: the first number
 * @b: the second number
 * Return: returns the quotient of the digits
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that modulo of a number
 * @a: the first number
 * @b: the second number
 * Return: returns the sum of the digits
 */
int op_mod(int a, int b)
{
	return (a % b);
}
